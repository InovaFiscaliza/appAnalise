                                                   'var_Band',                          'reportLibConnection.Variable.ClassProperty(reportInfo, analyzedData.InfoSet, "Band")', ...
                                                   'var_Description',                   'reportLibConnection.Variable.ClassProperty(reportInfo, analyzedData.InfoSet, "Description")', ...
                                                   'var_BeginTime',                     'reportLibConnection.Variable.ClassProperty(reportInfo, analyzedData.InfoSet, "BeginTime")', ...                                                   
                                                   'var_EndTime',                       'reportLibConnection.Variable.ClassProperty(reportInfo, analyzedData.InfoSet, "EndTime")', ...
                                                   'var_FreqStart',                     'reportLibConnection.Variable.ClassProperty(reportInfo, analyzedData.InfoSet, "FreqStart")', ...
                                                   'var_FreqStop',                      'reportLibConnection.Variable.ClassProperty(reportInfo, analyzedData.InfoSet, "FreqStop")', ...
                                                   'var_GPS',                           'reportLibConnection.Variable.ClassProperty(reportInfo, analyzedData.InfoSet, "GPS")', ...
                                                   'var_Location',                      'reportLibConnection.Variable.ClassProperty(reportInfo, analyzedData.InfoSet, "Location")', ...
                                                   'var_ObservationTime',               'reportLibConnection.Variable.ClassProperty(reportInfo, analyzedData.InfoSet, "ObservationTime")', ...
                                                   'var_Receiver',                      'reportLibConnection.Variable.ClassProperty(reportInfo, analyzedData.InfoSet, "Receiver")', ...
                                                   'var_RelatedFiles',                  'reportLibConnection.Variable.ClassProperty(reportInfo, analyzedData.InfoSet, "RelatedFiles")', ...
                                                   'var_StepWidth',                     'reportLibConnection.Variable.ClassProperty(reportInfo, analyzedData.InfoSet, "StepWidth")', ...
                                                   'var_Parameters',                    'reportLibConnection.Variable.ClassProperty(reportInfo, analyzedData.InfoSet, "Parameters")', ...
                                                   'var_TagFlow',                       'reportLibConnection.Variable.ClassProperty(reportInfo, analyzedData.InfoSet, "TagFlow")', ...
                                                   'var_TagEmission',                   'reportLibConnection.Variable.ClassProperty(reportInfo, analyzedData.InfoSet, "TagEmission")', ...
                                                   'var_TagChannel',                    'reportLibConnection.Variable.ClassProperty(reportInfo, analyzedData.InfoSet, "TagChannel")', ...
                                                   'var_RelatedLocations',              'reportLibConnection.Variable.GlobalProperty(reportInfo.Object, "RelatedLocations")', ...
                                                   'tbl_MonitoringBandsOverview',       'reportLibConnection.Table.MonitoringBandsOverview(reportInfo)', ...
                                                   'tbl_BandAlgorithms',                'reportLibConnection.Table.BandAlgorithms(analyzedData)', ...
                                                   'tbl_BandEmissions',                 'reportLibConnection.Table.BandEmissions(reportInfo, analyzedData)', ...
                                                   'tbl_EmissionPoints',                'reportLibConnection.Table.EmissionPoints(reportInfo, analyzedData)', ...
                                                   'tbl_Channel',                       'reportLibConnection.Table.Channel(reportInfo, analyzedData)', ...
                                                   'tbl_ChannelEmissions',              'reportLibConnection.Table.ChannelEmissions(reportInfo, analyzedData)', ...
                                                   'tbl_GlobalEmissions',               'reportLibConnection.Table.GlobalEmissions(dataOverview)', ...
                                                   'tbl_GlobalEmissionSummary',         'reportLibConnection.Table.GlobalEmissionSummary(reportInfo.Object, "Summary")', ...
                                                   'tbl_GlobalEmissionsIrregular',      'reportLibConnection.Table.GlobalEmissionSummary(reportInfo.Object, "Irregular")', ...
                                                   'tbl_CustomClassPropertyTable',      'reportLibConnection.Table.CustomClassPropertyTable(reportInfo, tableSettings)', ...
                                                   'img_Plot',                          'reportLibConnection.Plot.Controller(reportInfo, analyzedData, imgSettings)'), ...
                                'Project',  projectData, ...
                                'Context',  context, ...
                                'Object',   specData, ...
                                'Settings', generalSettings);

            fieldsUnnecessary = {'rootFolder', 'entryPointFolder', 'tempSessionFolder', 'ctfRoot'};
            fieldsUnnecessary(cellfun(@(x) ~isfield(reportInfo.Version.application, x), fieldsUnnecessary)) = [];
            if ~isempty(fieldsUnnecessary)
                reportInfo.Version.application = rmfield(reportInfo.Version.application, fieldsUnnecessary);
            end

            %-------------------------------------------------------------%
            % dataOverview
            %
            % Caso dataOverview não seja escalar e exista um item no relatório
            % com recorrência, a própria lib cria a variável "var_Index", acessível 
            % em "reportInfo.Function.var_Index".
            %-------------------------------------------------------------%
            dataOverview = struct('ID', {}, 'InfoSet', {}, 'HTML', {});

            referenceTable = buildSpectrumReferenceTable(specData, 1:numel(specData));
            sortedIdxs = referenceTable.Idx;
            specData = specData(sortedIdxs);

            for ii = 1:numel(specData)
                dataOverview(end+1) = struct( ...
                    'ID', sprintf('%.3f - %.3f MHz', specData(ii).MetaData.FreqStart / 1e6, specData(ii).MetaData.FreqStop / 1e6), ...
                    'InfoSet', specData(ii), ...
                    'HTML', struct('Component', {}, 'Source', {}, 'Value', {}) ...
                );
            end

            %-------------------------------------------------------------%
            % Conexão com reportLib, parte do repositório "SupportPackages"
            %-------------------------------------------------------------%
            HTMLDocContent = reportLib.Controller(reportInfo, dataOverview);

            %-------------------------------------------------------------%
            % Exclui container criado para os plots, caso aplicável.
            %-------------------------------------------------------------%
            hFigure    = mainApp.UIFigure;
            hContainer = findobj(hFigure, 'Tag', 'reportGeneratorContainer');
            if ~isempty(hContainer)
                delete(hContainer)
            end

            %-------------------------------------------------------------%
            % Em sendo a versão "Preliminar", apenas apresenta o html no
            % navegador. Por outro lado, em sendo a versão "Definitiva",
            % salva-se o arquivo ZIP em pasta local.
            %-------------------------------------------------------------%
            correlationKey     = char(matlab.lang.internal.uuid());
            sharepointFileBase = sprintf('%s_%s_%s',  appName, datestr(now, 'yyyymmdd'), correlationKey);
            [~, zipFileBase]   = appEngine.util.DefaultFileName('', [appName '_FinalReport'], issueId);

            HTMLFile = fullfile(generalSettings.fileFolder.tempPath, [sharepointFileBase '.html']);
            writematrix(HTMLDocContent, HTMLFile, 'QuoteStrings', 'none', 'FileType', 'text', 'Encoding', 'UTF-8')

            switch docVersion
                case 'preview'
                    web(HTMLFile, '-new')
                    updateGeneratedFiles(projectData, context)

                case 'final'
                    try
                        [issueDetails, msgError] = getOrFetchIssueDetails(projectData, projectData.modules.(context).ui.system, projectData.modules.(context).ui.issue, mainApp.eFiscalizaObj);

                        if ~isempty(msgError)
                            error('reportLibConnection:Controller', msgError)
                        end

                        if ~isdeployed() && ~ismember(issueDetails.issueContext.solicitacao.classificacao.macrotema, generalSettings.reportLib.allowedMacrothemes)
                            issueDetails.issueContext.solicitacao.classificacao.macrotema = generalSettings.reportLib.allowedMacrothemes{1};
                        end

                    catch ME
                        if ~isdeployed()
                            issueDetails = struct( ...
                                'usuario', struct('nome', 'NOME_FISCAL', 'email', 'EMAIL_FISCAL@anatel.gov.br', 'unidade', 'LOTACAO_FISCAL', 'funcao', 'FISCAL'), ...
                                'issueContext', struct( ...
                                    'solicitacao', struct('classificacao', struct('macrotema', generalSettings.reportLib.allowedMacrothemes{1})), ...
                                    'acao', struct('sei', struct('processo', '00000.000000/0000-00')) ...
                                ) ...
                            );
                        else
                            rethrow(ME)
                        end
                    end

                    macrotheme = issueDetails.issueContext.solicitacao.classificacao.macrotema;
                    if ~ismember(macrotheme, generalSettings.reportLib.allowedMacrothemes)
                        error('reportLibConnection:Controller:UnexpectedMacrothemes', 'O macrotema "%s" não está configurado para geração de relatórios.', macrotheme)
                    end

                    JSONFile  = '';
                    TEAMSFile = '';
                    XLSXFile  = '';
                    RAWFiles  = {};
                    ZIPFile   = ui.Dialog(callingApp.UIFigure, 'uiputfile', '', {'*.zip', [appName ' (*.zip)']}, fullfile(generalSettings.fileFolder.userPath, [zipFileBase '.zip']));
                    if isempty(ZIPFile)
                        return
                    end

                    ZIPFileList = {HTMLFile};

                    if strcmp(context, 'PLAYBACK')
                        JSONFile     = fullfile(generalSettings.fileFolder.tempPath, [sharepointFileBase '.json']);
                        TEAMSFile    = fullfile(generalSettings.fileFolder.tempPath, [sharepointFileBase '.teams']);

                        JSONContent  = reportLibConnection.Table.scarabJsonFile(projectData, context, specData, correlationKey, mainApp.executionMode, issueDetails);
                        TEAMSContent = reportLibConnection.Table.scarabTeamsFileContent(projectData, context, specData, issueDetails, {[sharepointFileBase '.json']});

                        writematrix(JSONContent,  JSONFile,  "FileType", "text", "QuoteStrings", "none", "WriteMode", "overwrite", "Encoding", "UTF-8")
                        writematrix(TEAMSContent, TEAMSFile, "FileType", "text", "QuoteStrings", "none", "WriteMode", "overwrite", "Encoding", "UTF-8")

                        ZIPFileList = [ZIPFileList, {JSONFile, TEAMSFile}];
                    end
                    
                    zip(ZIPFile, ZIPFileList)

                    generatedFileId = model.ProjectBase.computeReportHash(specData);
                    updateGeneratedFiles(projectData, context, generatedFileId, RAWFiles, HTMLFile, JSONFile, XLSXFile, TEAMSFile, ZIPFile)
            end
        end
    end

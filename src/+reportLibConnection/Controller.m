                                                   'eFiscaliza_solicitacaoClasse',      'reportLib.getEFiscalizaIssueParameter(reportInfo, "Solicitação de Inspeção", "classe")', ...
                                                   'eFiscaliza_solicitacaoSeiProcesso', 'reportLib.getEFiscalizaIssueParameter(reportInfo, "Solicitação de Inspeção", "processo")', ...
                                                   ...
                                                   'eFiscaliza_acaoId',                 'reportLib.getEFiscalizaIssueParameter(reportInfo, "Ação de Inspeção",        "id")', ...
                                                   'eFiscaliza_acaoCodigo',             'reportLib.getEFiscalizaIssueParameter(reportInfo, "Ação de Inspeção",        "codigo")', ...
                                                   'eFiscaliza_acaoDescricao',          'reportLib.getEFiscalizaIssueParameter(reportInfo, "Ação de Inspeção",        "descricao")', ...
                                                   'eFiscaliza_acaoStatus',             'reportLib.getEFiscalizaIssueParameter(reportInfo, "Ação de Inspeção",        "status")', ...
                                                   'eFiscaliza_acaoDataInicio',         'reportLib.getEFiscalizaIssueParameter(reportInfo, "Ação de Inspeção",        "inicio")', ...
                                                   'eFiscaliza_acaoDataFim',            'reportLib.getEFiscalizaIssueParameter(reportInfo, "Ação de Inspeção",        "fim")', ...
                                                   'eFiscaliza_acaoUnidade',            'reportLib.getEFiscalizaIssueParameter(reportInfo, "Ação de Inspeção",        "unidade")', ...
                                                   'eFiscaliza_acaoResponsavel',        'reportLib.getEFiscalizaIssueParameter(reportInfo, "Ação de Inspeção",        "responsavel")', ...
                                                   'eFiscaliza_acaoSeiProcesso',        'reportLib.getEFiscalizaIssueParameter(reportInfo, "Ação de Inspeção",        "processo")', ...
                                                   'eFiscaliza_acaoSeiRelatorio',       'reportLib.getEFiscalizaIssueParameter(reportInfo, "Ação de Inspeção",        "relatorio")', ...
                                                   'eFiscaliza_acaoSeiDespacho',        'reportLib.getEFiscalizaIssueParameter(reportInfo, "Ação de Inspeção",        "despacho")', ...
                                                   'eFiscaliza_acaoSeiPado',            'reportLib.getEFiscalizaIssueParameter(reportInfo, "Ação de Inspeção",        "pado")', ...
                                                   ...
                                                   'eFiscaliza_atividadeId',            'reportLib.getEFiscalizaIssueParameter(reportInfo, "Atividade de Inspeção",   "id")', ...
                                                   'eFiscaliza_atividadeCodigo',        'reportLib.getEFiscalizaIssueParameter(reportInfo, "Atividade de Inspeção",   "codigo")', ...
                                                   'eFiscaliza_atividadeDescricao',     'reportLib.getEFiscalizaIssueParameter(reportInfo, "Atividade de Inspeção",   "descricao")', ...
                                                   'eFiscaliza_atividadeStatus',        'reportLib.getEFiscalizaIssueParameter(reportInfo, "Atividade de Inspeção",   "status")', ...
                                                   'eFiscaliza_atividadeDataInicio',    'reportLib.getEFiscalizaIssueParameter(reportInfo, "Atividade de Inspeção",   "inicio")', ...
                                                   'eFiscaliza_atividadeDataFim',       'reportLib.getEFiscalizaIssueParameter(reportInfo, "Atividade de Inspeção",   "fim")', ...
                                                   'eFiscaliza_atividadeFiscalLider',   'reportLib.getEFiscalizaIssueParameter(reportInfo, "Atividade de Inspeção",   "principal")', ...
                                                   ...
                                                   'eFiscaliza_InformacaoBruta',        'reportLib.getEFiscalizaIssueParameter(reportInfo, "Informação Bruta")', ...
                                                   'eFiscaliza_Demandante',             'reportLib.getEFiscalizaIssueParameter(reportInfo, "Nome do Demandante")', ...
                                                   'eFiscaliza_UnidadeExecutante',      'reportLib.getEFiscalizaIssueParameter(reportInfo, "Nome da Unidade Executante")', ...
                                                   'eFiscaliza_SedeUnidade',            'reportLib.getEFiscalizaIssueParameter(reportInfo, "Sede da Unidade Executante")', ...
                                                   'eFiscaliza_PeriodoPrevisto',        'reportLib.getEFiscalizaIssueParameter(reportInfo, "Período Previsto da Fiscalização")', ...
                                                   'eFiscaliza_ListaEntidades',         'reportLib.getEFiscalizaIssueParameter(reportInfo, "Lista de Entidades")', ...
                                                   'eFiscaliza_ListaServicos',          'reportLib.getEFiscalizaIssueParameter(reportInfo, "Lista de Serviços")', ...
                                                   'eFiscaliza_ListaFiscais',           'reportLib.getEFiscalizaIssueParameter(reportInfo, "Lista de Fiscais")', ...
                                                   ...
                                                   ... % APLICÁVEIS À SEÇÃO COM RECORRÊNCIA DO RELATÓRIO
                                                   'var_Index',                         '', ...
                                                   'var_IndexEmission',                 [], ...
                                                   'var_IndexChannel',                  [], ...

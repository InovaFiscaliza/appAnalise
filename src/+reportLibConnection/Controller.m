                                                   'var_EntityGroupId',                  projectData.modules.(context).ui.entity.id, ...
                                                   ...
                                                   'eFiscaliza_solicitacaoId',          'reportLib.getEFiscalizaIssueParameter(reportInfo, "Solicitação de Inspeção", "id")', ...
